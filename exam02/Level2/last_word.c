#include <unistd.h>

int main(int ac, char **av){
    if (ac == 2){
        int i = 0;
        char *lw;
        while (av[1][i]){
            if (av[1][i] <= 32 && av[1][i + 1] >  32)
                lw = &av[1][i + 1];
            i++;
        }
        i = 0;
        while (lw[i] && lw[i] != 32){
            write (1, &lw[i], 1);
            i++;
        }
    }
    write (1, "\n", 1);
    return 0;
}