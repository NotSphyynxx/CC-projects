char    *ft_strrev(char *str){
    int i = 0;
    while (str[i + 1])
        i++;
    int j = 0;
    while (i > j){
        char tmp = str[j];
        str[j] = str[i];
        str[i] = tmp;
        i--;
        j++;
    }
    return (str);
}